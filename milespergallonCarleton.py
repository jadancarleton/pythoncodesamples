#This program calculates miles/gallon from a number of miles and gallons



miles = int(input("Hello there! Please input the number of miles you have driven: "))

gallons = int(input("Now, please input how much gas you have used, in gallons: "))

mpg = (miles / gallons)

print("Thank you! Your car currently gets " + str(mpg) + " miles per gallon.")
